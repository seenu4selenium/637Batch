package corejava;

public class LoopingStatements {
	public static void main(String[] args) {

		// Print 1 to 10 numbers
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);// 12345678910
		} // for loop ends
		System.out.println("***************");
		
		for (int i = 10;i>0;i--) {
			System.out.println(i);//10987654321
		}
	}

}
